export function t(lang: SupportedLang, key: string): string {
  const table = STRINGS[lang] || STRINGS.en;
  return table[key] || STRINGS.en[key] || key;
}

export function detectLangFromPath(pathname: string): SupportedLang {
  const seg = pathname.split("/").filter(Boolean)[0];
  const supported = ["en", "ar", "zh", "ru", "de", "fr", "hi"] as const;
  return (supported.includes(seg as any) ? (seg as SupportedLang) : "en");
}


